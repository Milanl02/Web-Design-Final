//For Hamburger Menu:
var toggle=false;

document.body.querySelector(".menuIcon").addEventListener("click", function (){

    toggle=!toggle;

    if(toggle){
        document.body.querySelector(".menu").style.right="0";
    }else{
        document.body.querySelector(".menu").style.right="-160px";
    }
})

//For Dodge Charger:
var charger=false;

document.body.querySelector(".carbox").addEventListener("click", function (){

    charger=!charger;

    if(charger) {
        document.body.querySelector(".chargerbox").style.left = "418px";
    }else{
        document.body.querySelector(".chargerbox").style.left="-1000px";
    }
})

